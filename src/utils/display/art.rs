pub fn get_display_letter_from_char(c:char) -> [[u8;5];5] {
    if (c.is_ascii_digit() || c.is_ascii_punctuation() || c.is_ascii_control()) && c != ' '  {
        return WARNING
    }
    let new_char = c.to_ascii_lowercase();
    let letter:[[u8;5];5] = match new_char {
        'a' => LED_A,
        'b' => LED_B,
        'c' => LED_C,
        'd' => LED_D,
        'e' => LED_E,
        'f' => LED_F,
        'g' => LED_G,
        'h' => LED_H,
        'i' => LED_I,
        'j' => LED_J,
        'k' => LED_K,
        'l' => LED_L,
        'm' => LED_M,
        'n' => LED_N,
        'o' => LED_O,
        'p' => LED_P,
        'q' => LED_Q,
        'r' => LED_R,
        's' => LED_S,
        't' => LED_T,
        'u' => LED_U,
        'v' => LED_V,
        'w' => LED_W,
        'x' => LED_X,
        'y' => LED_Y,
        'z' => LED_Z,
        ' ' => BLANK,
        '1' => ONE,
        '2' => TWO,
        '3' => THREE,
        '4' => FOUR,
        '5' => FIVE,
        '6' => SIX,
        '7' => THINGAFTERSIX,
        '8' => EIGHT,
        '9' => NINE,
        '0' => ZERO,
        _ => WARNING
        
    };


    return letter
}